void formiraj_sudoku(char p[82], int matrica[9][9])
{
    for (int i = 0; i < 81; i++)
    {
        matrica[i / 9][i % 9] = p[i] - '0';
    }
}

int ispitati_red(int matrica[][9], int red, int br)
{
    for (int kolona = 0; kolona < 9; kolona++)
        if (matrica[red][kolona] == br)
            return 1;
    return 0;
}

int ispitati_kolonu(int matrica[][9], int kolona, int br)
{
    for (int red = 0; red < 9; red++)
        if (matrica[red][kolona] == br)
            return 1;
    return 0;
}

int ispitati_mini_kvadrat(int matrica[][9], int pocetni_red, int pocetna_kolona, int br)
{
    for (int red = 0; red < 3; red++)
        for (int kolona = 0; kolona < 3; kolona++)
            if (matrica[pocetni_red + red][pocetna_kolona + kolona] == br)
                return 1;
    return 0;
}

void ispisi_matricu(int matrica[][9], int n, int m)
{
    for (int i = 0; i < n; i++)
    {
        for (int j = 0; j < m; j++)
            printf(" %d ", matrica[i][j]);
        printf("\n");
    }
}

int da_li_smo_dobili_br(int matrica[][9], int red, int kolona, int broj)
{
    if (!ispitati_red(matrica, red, broj) && !ispitati_kolonu(matrica, kolona, broj) &&
        !ispitati_mini_kvadrat(matrica, red - red % 3, kolona - kolona % 3, broj) && matrica[red][kolona] == 0)
        return 1;
    return 0;
}

int rijesi_sudoku(int matrica[][9])
{
    int red, kolona, pronadjen = 0;

    for (int i = 0; i < 9 && pronadjen == 0; i++)
        for (int j = 0; j < 9 && pronadjen == 0; j++)
            if (matrica[i][j] == 0)
            {
                red = i;
                kolona = j;
                pronadjen = 1;
            }

    if (pronadjen == 0)
        return 1;

    for (int broj = 1; broj <= 9; broj++)
    {

        if (da_li_smo_dobili_br(matrica, red, kolona, broj))
        {

            matrica[red][kolona] = broj;

            if (rijesi_sudoku(matrica))
                return 1;

            matrica[red][kolona] = 0;
        }
    }

    return 0;
}

